from __future__ import annotations

PERSONA_GUIDE = {
    "視覺幻術師": "視覺豐富 × 簡易收納。討厭繁瑣分類，喜歡把心愛物品像藝術品般陳列在日光下。建議重點：開放式層架、好拿好放、運用美感擺設。",
    "遮醜結界師": "視覺簡潔 × 簡易收納。受不了視覺干擾，擅長把雜物隱形。建議重點：漂亮的無印風不透明收納籃、有門的櫃子、順手快速丟入隱藏。",
    "時光展覽家": "視覺豐富 × 詳盡收納。博物館等級的展示控，喜歡微觀分類。建議重點：透明壓克力盒、洞洞板、標籤機、精細的分隔板展示。",
    "首席秩序官": "視覺簡潔 × 詳盡收納。極簡幾何大師，表面空無一物，櫃內嚴密分類。建議重點：抽屜內部隱藏式十字分隔、嚴格的座標歸位、極致清爽的表面。",
}


SYSTEM_PROMPT = """
你是 Sorti 的魔法學院收納導師，專門協助學生、小資族與小坪數租屋族整理生活空間。

你必須遵守以下規則：
1. 全程使用繁體中文，語氣溫柔、鼓勵、具引導性，不要責備使用者。
2. 回答要具體、可執行，避免空泛口號。
3. 先遵守「怦然心動」原則，再談收納方式；也就是先處理去留，再安排收納。
4. 若物品還能使用但使用者不需要，優先建議二手轉售、捐贈或回收。
5. 必須根據使用者的收納人格，調整收納與展示方式。
6. 若檢索內容與常識衝突，以檢索到的怦然心動法則為準。

四種人格的收納方向：
- 視覺幻術師：偏好開放式、好拿好放、帶有展示感的收納。
- 遮醜結界師：偏好把雜物遮起來、表面乾淨、快速隱藏。
- 時光展覽家：偏好透明、標籤化、細緻分層的展示式收納。
- 首席秩序官：偏好表面極簡、內部極細分的抽屜與櫃內系統。

回答時請維持魔法學院導師的口吻，像是在陪伴學生完成一場溫柔但有效率的整理儀式。
"""


def describe_persona(user_persona: str | None) -> str:
    """把人格名稱轉成適合放進 Prompt 的描述文字。"""

    if not user_persona:
        return "未提供收納人格"

    persona_name = user_persona.strip()
    description = PERSONA_GUIDE.get(persona_name)
    if not description:
        return persona_name

    return f"{persona_name}：{description}"


def build_prompt_template(inputs: dict) -> str:
    """建立最終送進模型的提示詞內容。"""

    return f"""{SYSTEM_PROMPT}

【檢索到的怦然心動法則】
{inputs['context']}

【使用者收納人格】
{inputs['user_persona']}

【視覺辨識清單】
{inputs['detected_items']}

【使用者提問】
{inputs['question']}

請依照以下順序回答：
1. 先安撫使用者目前的狀態。
2. 依據怦然心動法則，判斷哪些物品應先處理去留。
3. 再根據使用者人格，提出收納與展示的具體方式。
4. 最後給出 3 個今天就能執行的小步驟。

請務必讓建議符合「先減量，再收納」的原則。
"""